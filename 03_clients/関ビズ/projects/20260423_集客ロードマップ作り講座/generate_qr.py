# QRコード生成
qr_files = {}
for name, text in prompts.items():
    # テキストのバイト数を確認
    byte_len = len(text.encode('utf-8'))
    print(f"{name}: {byte_len} bytes")

    qr = qrcode.QRCode(
        version=None,  # 自動でサイズ調整
        error_correction=qrcode.constants.ERROR_CORRECT_L,  # 低エラー訂正（データ容量優先）
        box_size=10,
        border=2,
    )
    qr.add_data(text)
    qr.make(fit=True)
    print(f"  → QR version: {qr.version}")

    img = qr.make_image(fill_color="black", back_color="white")
    filepath = os.path.join(OUT_DIR, f"qr_{name}.png")
    img.save(filepath)
    qr_files[name] = filepath
    print(f"  → saved: {filepath}")

# base64エンコードしてHTMLに埋め込むためのデータを生成
print("\n--- Base64 data for HTML embedding ---")
b64_data = {}
for name, filepath in qr_files.items():
    with open(filepath, "rb") as f:
        b64 = base64.b64encode(f.read()).decode("ascii")
        b64_data[name] = b64
        print(f"{name}: {len(b64)} chars (base64)")

# 配布物HTMLを更新
handout_path = os.path.join(OUT_DIR, "配布物_FELOプロンプト.html")
with open(handout_path, "r", encoding="utf-8") as f:
    html = f.read()

# QRプレースホルダーを実画像に置き換え
replacements = [
    ('QR①<br>ステップ1', 'step1', 'ステップ1'),
    ('QR②<br>ステップ2', 'step2', 'ステップ2'),
    ('QR③<br>ステップ3', 'step3', 'ステップ3'),
    ('QR④<br>ステップ4', 'step4', 'ステップ4'),
]

for placeholder_text, key, label in replacements:
    old_div = f'<div class="qr-placeholder">{placeholder_text}</div>'
    new_img = f'<img src="data:image/png;base64,{b64_data[key]}" style="width:100px; height:100px; border-radius:4px;" alt="{label}">'
    html = html.replace(old_div, new_img)

# QRコード用の注意書きも更新
html = html.replace(
    '※ QRコードはGoogle Driveやメモ帳のリンクに差し替えてください。講師側で事前準備が必要です。',
    '※ QRコードを読み取ると、プロンプト全文がテキストとして表示されます。〇〇の部分だけ書き換えてFELOに貼り付けてください。'
)

with open(handout_path, "w", encoding="utf-8") as f:
    f.write(html)

print("\n✅ 配布物HTMLにQRコードを埋め込みました")
